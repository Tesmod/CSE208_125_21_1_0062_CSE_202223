#Python codes to perform the power of a number

base = 2
exponent = -1
result = pow(base, exponent)
print(f"The result of {base} raised to the power of {exponent} is: {result}")

#Another method to perform this is given below which also gives the same result

base = 2
exponent = 0
result = base ** exponent
print(f"The result of {base} raised to the power of {exponent} is: {result}")